/////////////////////////////////////////////////////////////////////////////
// File upload
/////////////////////////////////////////////////////////////////////////////
function fileUploadControl(){
	global $db;
	extract($_REQUEST);
	$data	=	array(
					'file' => $_FILES["image"],
					'target_dir' => "upload/",
					'type' => 'png,jpg',
					'size' => '1 MB,5 MB',
					);
	$fdata = fileUpload($data);
}


/////////////////////////////////////////////////////////////////////////////
// File remove
/////////////////////////////////////////////////////////////////////////////
function fileRemoveControl()
{
	$data	=	array(
				'file' => $fdata['file']['path'],
				'target_dir' => "upload",					
				);
	$fdata = fileDelete($data);
}

